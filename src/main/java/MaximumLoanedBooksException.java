public class MaximumLoanedBooksException extends Exception {
    public MaximumLoanedBooksException() {
    }

    public MaximumLoanedBooksException(String message) {
        super(message);
    }

    public MaximumLoanedBooksException(String message, Throwable cause) {
        super(message, cause);
    }

    public MaximumLoanedBooksException(Throwable cause) {
        super(cause);
    }

    public MaximumLoanedBooksException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
